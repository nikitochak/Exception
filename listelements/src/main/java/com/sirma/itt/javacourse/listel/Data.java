package com.sirma.itt.javacourse.listel;

public class Data {
	private int number;
	private String name;

	/**
	 * Getters and setters for the attributes.
	 */
	public void setName(String name) {
		this.name = name;
	}

	public void setNum(int number) {
		this.number = number;
	}

	public int getNum() {
		return this.number;
	}

	public String getName() {
		return this.name;
	}

}
